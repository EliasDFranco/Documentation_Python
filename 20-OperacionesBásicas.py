def Suma(n1, n2):
    return n1 + n2
def Resta(n1, n2):
    return n1 - n2
def Multi(n1,n2):
    return n1 * n2 
def Divi(n1, n2):
    return n1 / n2 
def modulo(n1 , n2 ):
    return n1 % n2

n1 = 20
n2 = 10

print("La suma es: ", Suma(n1 , n2))
print("La resta es: ", Resta(n1 , n2))
print("La multiplicación es: ", Multi(n1 , n2))
print("La división es: ", Divi(n1 , n2))
print("El módulo es: ", modulo(n1 , n2))
